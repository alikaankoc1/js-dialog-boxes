//! Alert: In JavaScript, alert() displays a popup window with a warning message.
alert("Refresh the page with the F5 key.");

//! Prompt: In JavaScript, prompt() displays a popup window to get input from the user.

let age = prompt("Enter your age:");
console.log(age);
console.log(typeof age);
//? Values ​​received from the user are always returned as strings.

//! Confirm: In JavaScript, confirm() displays a confirmation box where the user can respond with "OK" or "Cancel."

let approval = confirm("Do you confirm the transaction?");
console.log(approval);
console.log(typeof approval);







